#!/usr/bin/env bash
# MUFREZE — EXP Sync
# Bidirectional sync between Elder MCP experiences and MUFREZE experiences
# Usage: exp-sync.sh [direction]
#   direction: "pull" (elder→mufreze), "push" (mufreze→elder), "both" (default)

set -euo pipefail

readonly E_SUCCESS="✅"
readonly E_FAILURE="❌"
readonly E_MEDAL="🎖️"

direction="${1:-both}"

ELDER_DIR="/Users/mac/.claude/claude-memory/experiences"
MUFREZE_GLOBAL_DIR="${MUFREZE_HOME:-$HOME/.mufreze}/experiences"
SYNC_INDEX="$MUFREZE_GLOBAL_DIR/.sync-index"

mkdir -p "$MUFREZE_GLOBAL_DIR"

# ---------------------------------------------------------------------------
# Count EXPs
# ---------------------------------------------------------------------------
count_exps() {
    local dir="$1"
    find "$dir" -maxdepth 1 -name "EXP-*.md" 2>/dev/null | wc -l | tr -d ' '
}

# ---------------------------------------------------------------------------
# Sync direction: Elder → MUFREZE (pull)
# ---------------------------------------------------------------------------
sync_pull() {
    local pulled=0

    if [[ ! -d "$ELDER_DIR" ]]; then
        echo "${E_FAILURE} Elder directory not found: $ELDER_DIR" >&2
        return 1
    fi

    for exp_file in "$ELDER_DIR"/EXP-*.md; do
        [[ -f "$exp_file" ]] || continue
        local basename
        basename=$(basename "$exp_file")
        local target="$MUFREZE_GLOBAL_DIR/elder-${basename}"

        if [[ ! -f "$target" ]] || [[ "$exp_file" -nt "$target" ]]; then
            cp "$exp_file" "$target"
            pulled=$((pulled + 1))
        fi
    done

    echo "${E_MEDAL} Pull: $pulled EXP(s) synced from Elder → MUFREZE"
}

# ---------------------------------------------------------------------------
# Sync direction: MUFREZE → Elder (push)
# ---------------------------------------------------------------------------
sync_push() {
    local pushed=0

    mkdir -p "$ELDER_DIR"

    for exp_file in "$MUFREZE_GLOBAL_DIR"/EXP-*.md; do
        [[ -f "$exp_file" ]] || continue
        local basename
        basename=$(basename "$exp_file")
        local target="$ELDER_DIR/mufreze-${basename}"

        if [[ ! -f "$target" ]] || [[ "$exp_file" -nt "$target" ]]; then
            cp "$exp_file" "$target"
            pushed=$((pushed + 1))
        fi
    done

    echo "${E_MEDAL} Push: $pushed EXP(s) synced from MUFREZE → Elder"
}

# ---------------------------------------------------------------------------
# Build unified search index
# ---------------------------------------------------------------------------
build_index() {
    local total=0
    echo "# MUFREZE Unified EXP Index" > "$SYNC_INDEX"
    echo "# Auto-generated by exp-sync.sh — $(date -u +"%Y-%m-%dT%H:%M:%SZ")" >> "$SYNC_INDEX"
    echo "" >> "$SYNC_INDEX"

    # MUFREZE global
    echo "## MUFREZE Global" >> "$SYNC_INDEX"
    for exp_file in "$MUFREZE_GLOBAL_DIR"/EXP-*.md; do
        [[ -f "$exp_file" ]] || continue
        local title
        title=$(head -5 "$exp_file" | grep -m1 "^#\|^title:" | sed 's/^#\+ *//' | sed 's/^title: *//' || basename "$exp_file")
        echo "- [$(basename "$exp_file")] $title" >> "$SYNC_INDEX"
        total=$((total + 1))
    done

    # Elder synced
    echo "" >> "$SYNC_INDEX"
    echo "## Elder (Synced)" >> "$SYNC_INDEX"
    for exp_file in "$MUFREZE_GLOBAL_DIR"/elder-EXP-*.md; do
        [[ -f "$exp_file" ]] || continue
        local title
        title=$(head -5 "$exp_file" | grep -m1 "^#\|^title:" | sed 's/^#\+ *//' | sed 's/^title: *//' || basename "$exp_file")
        echo "- [$(basename "$exp_file")] $title" >> "$SYNC_INDEX"
        total=$((total + 1))
    done

    echo "" >> "$SYNC_INDEX"
    echo "Total: $total EXPs" >> "$SYNC_INDEX"
    echo "${E_MEDAL} Index: $total EXP(s) indexed"
}

# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------
echo "${E_MEDAL} MUFREZE EXP Sync" >&2
echo "  Elder:   $ELDER_DIR ($(count_exps "$ELDER_DIR") EXPs)" >&2
echo "  MUFREZE: $MUFREZE_GLOBAL_DIR ($(count_exps "$MUFREZE_GLOBAL_DIR") EXPs)" >&2
echo "" >&2

case "$direction" in
    pull)
        sync_pull
        ;;
    push)
        sync_push
        ;;
    both)
        sync_pull
        sync_push
        ;;
    *)
        echo "${E_FAILURE} Unknown direction: $direction (use: pull, push, both)" >&2
        exit 1
        ;;
esac

build_index
echo "${E_SUCCESS} Sync complete"
